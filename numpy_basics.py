import numpy as np

# Q1: Sum of all elements
arr1 = np.array([10, 20, 30])
print("Q1 - Sum of elements:", np.sum(arr1))

# Q2: Shape of array
arr2 = np.array([[1, 2, 3], [4, 5, 6]])
print("Q2 - Shape of array:", arr2.shape)

# Q3: Maximum element
arr3 = np.array([5, 12, 3, 9])
print("Q3 - Maximum element:", np.max(arr3))

# Q4: Add two arrays
arr4 = np.array([1, 2, 3])
arr5 = np.array([4, 5, 6])
print("Q4 - Sum of arrays:", np.add(arr4, arr5))

# Q5: Data type of array
arr6 = np.array([1, 2, 3])
print("Q5 - Data type:", arr6.dtype)
